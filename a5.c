#include<stdio.h>

int main(){

    // char to int
    char a = 'A';
    int b = a;
    printf("char to int :%d \n", b);
    // int to char
    int c = 65;
    char d = c;
    printf("int to char :%c \n", d);
    // int to float 
    int e = 10;
    float f = e;
    printf("int to float : %f \n" , f);

    //float to int 
    float g = 9.8;
    int h = g;
    printf("float to int ; %d \n", h);
}